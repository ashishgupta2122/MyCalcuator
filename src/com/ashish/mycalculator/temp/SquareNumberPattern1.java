package com.ashish.mycalculator.temp;
import java.util.*;
public class SquareNumberPattern1 {
    public static void main(String[] args)
    {
        int n = 4;
        for (int i = 1; i <= n; i++)
        {
            for (int j = 1; j <= n; j++)
            {
                System.out.print(j+" ");
            }
            System.out.println();
        }
    }
}
